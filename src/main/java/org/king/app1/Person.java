package org.king.app1;

public class Person {
    private String name;
    private String address;
    private String title;
    private int age;
    private String gender;
    private String ethnicBackground;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEthnicBackground() {
        return ethnicBackground;
    }

    public void setEthnicBackground(String ethnicBackground) {
        this.ethnicBackground = ethnicBackground;
    }
}